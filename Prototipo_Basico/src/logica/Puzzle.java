package logica;

public class Puzzle {

	private String tipoPuzzle;
	private String estadoPuzzle;
	
	public void resolverPuzzle () {}
	public void moverObjeto () {}
	public void combinarElementos() {}
	public String getTipoPuzzle() {
		return tipoPuzzle;
	}
	public void setTipoPuzzle(String tipoPuzzle) {
		this.tipoPuzzle = tipoPuzzle;
	}
	public String getEstadoPuzzle() {
		return estadoPuzzle;
	}
	public void setEstadoPuzzle(String estadoPuzzle) {
		this.estadoPuzzle = estadoPuzzle;
	}
	
	
}
